    private void updateInfoButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateInfoButtonActionPerformed
        // TODO add your handling code here:
        String bday = months[1][Month.getSelectedIndex()]+Day.getSelectedItem()+Year.getSelectedItem();
        boolean errorOccured=false;
        if(newFirstName.getText().length()>40 || newFirstName.getText().length()<1){
            ErrorMessageDialog fNameError = new ErrorMessageDialog(this,true,"Error: First name is not valid.");
            fNameError.setVisible(true);
            newFirstName.setText("");
            errorOccured=true;
        }
        if(newLastName.getText().length()>40 || newLastName.getText().length()<1){
            ErrorMessageDialog lNameError = new ErrorMessageDialog(this,true,"Error: Last name is not valid.");
            lNameError.setVisible(true);
            newLastName.setText("");
            errorOccured=true;
        }
        if(newEmail.getText().length()>200 || newEmail.getText().length()<1){
            ErrorMessageDialog emailError = new ErrorMessageDialog(this,true,"Error: Email is not valid.");
            emailError.setVisible(true);
            newEmail.setText("");
            errorOccured=true;
        }
        if(newPhone.getText().length()>10 || newPhone.getText().length()<1){
            ErrorMessageDialog phoneError = new ErrorMessageDialog(this,true,"Error: Phone number is not valid.");
            phoneError.setVisible(true);
            newPhone.setText("");
            errorOccured=true;
        }
        if(!errorOccured){
            api.about(newFirstName.getText(), newLastName.getText(), bday, newPhone.getText(), newEmail.getText());
            System.out.print("user updated");
        }
    }//GEN-LAST:event_updateInfoButtonActionPerformed

