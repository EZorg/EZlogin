    private void parseArray(String[][] a){
        lNums= new String[a.length];
        model = new Object[a.length][a[0].length];
        dotmodel = new Object[a.length][a[0].length];
        boolean[] boxValues = api.loginStatus();
        for(int i=0;i<a.length;i++){
            lNums[i] = a[i][0]; 
        }
        for(int i=0;i<23;i++){
            model[i][0]=boxValues[i];
            dotmodel[i][0] = boxValues[i];
        }
        for(int i=0;i<a.length;i++){
            for(int j=1;j<a[i].length;j++){
                if(!showPassword.isSelected() && j==3)
                    dotmodel[i][j]= "••••••••••";
                else
                    dotmodel[i][j] = new String(a[i][j]);
                model[i][j] = new String(a[i][j]);
            }
        }
    }

